    public void PreencherTabela(String sql) {
        try {
            PreparedStatement stmt = con.prepareStatement(sql);
            if (sql.equals("select * from Produtos where nome ilike ?")) {
                String nome = tfCompra.getText();
                stmt.setString(1, "%" + nome + "%");
            } else {
                if (sql.equals("select * from Produtos where id_produto = ?")) {
                   int id = Integer.parseInt(tfID.getText());
                    stmt.setInt(1, id);
                }
            }

            ResultSet rs = stmt.executeQuery();

            DateFormat data = new SimpleDateFormat("dd/MM/yyyy");

            DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();

            modelo.setNumRows(0);

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getString("nome"),
                    rs.getInt("id_produto"),
                    data.format(rs.getDate("validade")),
                    rs.getDouble("valor_venda"),
                    rs.getDouble("valor_compra"),
                    rs.getString("generico"),
                    rs.getInt("quantidade")
                });
            }

            rs.close();
            stmt.close();

        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }

    }

