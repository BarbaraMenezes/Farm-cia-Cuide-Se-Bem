    private void buCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buCadastrarActionPerformed
        // TODO add your handling code here:
        Produto produtos = new Produto();
        String opcao = null;
        boolean verificadorrb = false;
        if (rbSim.isSelected()) {
            opcao = "S";
            verificadorrb = true;
        } else {
            if (rbNao.isSelected()) {
                opcao = "N";
                verificadorrb = true;
            }
        }
        if (verificadorrb) {
            
              try {
                  String venda = tfVenda.getText();
                  double valor_venda = Double.parseDouble(venda);
                  
                  String compra = tfCompra.getText();
                  double valor_compra = Double.parseDouble(compra);
                  
                  String qtd = tfQuantidade.getText();
                  int quantidade = Integer.parseInt(qtd);
                  
                 String cpf = InformacoesSistema.CPF ;
                  
                 if (produtos.Inserir(con, tfNome.getText(),cpf, tfValidade.getText(),valor_venda ,valor_compra, opcao,quantidade)) {
                    JOptionPane.showMessageDialog(null, "Cadastro Realizado com Sucesso", "Cadastro Realizado", 1);
                } else {
                    JOptionPane.showMessageDialog(null, "Cadastro Não Realizado", "Cadastro Não Realizado", 0);
                }
            } catch (SQLException ex) {
                Logger.getLogger(CadastrarProduto.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ParseException ex) {
                Logger.getLogger(CadastrarProduto.class.getName()).log(Level.SEVERE, null, ex);

            }

        } else {
            JOptionPane.showMessageDialog(null, "Selecione Se é Genérico ou Não", "Selecione Uma Opção", 0);
        }

        String sql = "select * from Produtos";
        PreencherTabela(sql);


    }//GEN-LAST:event_buCadastrarActionPerformed

