    private void jbLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbLoginActionPerformed
        // TODO add your handling code here:

        String senha,validar;
        senha = String.valueOf(pfSenha1.getPassword());
        Criptografia criptografia = new Criptografia();
        validar = criptografia.Criptografar(senha);
        String login = String.valueOf(tfCPF.getText());
        InformacoesSistema.CPF = login;
        Funcionario funcionario = new Funcionario();

        try {
            if (funcionario.VerificarLogin(con, login, validar)) {
                TelaPrincipal principal = new TelaPrincipal();
                principal.setVisible(true);
                conexao.Desconectar();
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "CPF e/ou Senha incorretos!", "Login inv√°lido!", 0);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jbLoginActionPerformed

